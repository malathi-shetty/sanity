package multioperator_functions;

public class AssignmentOperator {

	public static void main(String[] args) {
		int i=10;
		i+=3;// i=i+3;
		System.out.println(i);
		
		i-=3;//i=i-3;
		System.out.println(i);
		
		i*=3;//i=i*3;
		System.out.println(i);
		
		i/=3;//i=i/3;
		System.out.println(i);
		
		i%=3;//i=i%3;
		System.out.println(i);
		
		int j = 10+12*2/2; // precedence
		System.out.println(j);

	}

}
