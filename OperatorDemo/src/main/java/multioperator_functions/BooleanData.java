package multioperator_functions;

public class BooleanData {

	public static void main(String[] args) {
		// true/false
		
		boolean b1=true;
		System.out.println(b1);
		
		boolean b2=!true;
		System.out.println(b2);
		
		// values of boolean data-type itself is a keyword
		// memory size - 1 bit
	}

}
