package array;

public class Arrays {

	public static void main(String[] args) {
		// Arrays help us to store multiple elements of same data type
		
		
		char[] a = new char[5];
		a[0]='a';
		a[1]='b';
		a[2]='c';
		a[3]='d';
		a[4]='e';
		
		for(int i=0; i<5; i++) {
			System.out.println(a[i]);
		}

	}

}
