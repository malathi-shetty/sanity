package conditions;

public class Conditional1_ifElse_IfElse {

	public static void main(String[] args) {
		
		int time=17;
	
		if(time <10) {
			System.out.println("Good Morning");  // false
			}else if(time<20) {
				System.out.println("Good Evening"); // true
			}else {
				System.out.println("Good day.");  // false
			}

	}

}
