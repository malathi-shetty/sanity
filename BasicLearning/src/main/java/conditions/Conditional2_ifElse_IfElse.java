package conditions;

public class Conditional2_ifElse_IfElse {

	public static void main(String[] args) {
		
		int a=434;
		int b=37;
		if(b>a) {
			System.out.println("b is greater than a"); // false
		}else if(a==b) {
			System.out.println("a & b are equal");  // false
		} else {
			System.out.println("a is greater than b");  // true
		}

	}

}
